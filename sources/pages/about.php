<div>
   <h1>A propos de l'assistant de configuration PC</h1>

   <p>
   Bienvenu sur ACPC, l'assistant de configuration de PC !<br>
   Vous trouverez toutes les informations nécéssaires au fonctionnement
   de l'assistant ci-dessous.
   </p>

   <h3>Guide d'utilisation</h3>

   <p>
   Une fois ce court guide d’utilisation lu, rendez-vous sur la page
   d’accueil de notre site. Sur cette page, vous pourrez commencer
   à établir la configuration de votre PC.
   </p>

   <p>
   Pour vous guider au mieux sur vos besoins, nous avons juste besoin de
   connaître le rôle de votre ordinateur dans votre vie. Si vous l’utilisez
   de façon professionnelle, un ordinateur de bureautique est conseillé.
   Si c’est pour jouer, la catégorie « Gaming » est là pour vous. Bien
   qu’un ordinateur « Gaming » puisse faire de la bureautique, l’inverse
   n’est pas toujours vrai. De plus, nous vous proposons de nous faire part
   de votre budget afin de vous prévenir si vous avez les yeux plus gros que
   le ventre. Il ne vous reste plus qu’à valider.
   </p>

   <p>
   Une fois le premier filtre passé, nous vous proposons une liste de
   composants en précisant leurs caractéristiques principales et leur prix.
   Dès lors, c’est à vous de choisir.
   </p>

   <p>
   Peu importe le sens de sélection des composants, nous ajusterons les listes
   des composants restant afin de vous éviter l’achat de deux pièces
   incompatibles. Toutefois, il est préférable de commencer par la carte
   mère plutôt que le boîtier.
   </p>

   <p>
   Pour sélectionner un composant, ouvrez le menu déroulant correspondant. Une
   liste de composants va apparaître et vous pourrez cliquer sur « Ajouter » au
   bout de la ligne du composant choisi. Ce dernier apparaitra alors en haut du
   menu déroulant avec un bouton « Supprimer », si vous souhaitez changer
   d’avis. De plus, lors de l’aperçu des listes déroulantes des composants,
   vous pouvez voir quels composants sont présent ou manquant dans votre
   configuration. Vous trouverez au bout du titre du menu déroulant un vu si
   un composant est sélectionné.
   </p>

   <center>
   <img src="<?php echo ROOT; ?>/assets/img/example.png" alt="imageLoris" height="60" width="400">
   </center><br>

   <p>
   Rien ne vous oblige à sélectionner un composant de chaque catégorie. Vous
   trouverez un bouton en bas de page vous permettant d’exporter votre
   configuration à tout moment.
   </p>

   <p>
   Merci d’utiliser ACPC et au plaisir de vous revoir.
   </p>

</div>
